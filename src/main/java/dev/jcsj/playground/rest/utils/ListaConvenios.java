package dev.jcsj.playground.rest.utils;

public class ListaConvenios {

    private long codigoMCI;
    private long numeroConvenio;
    private String name;

    public long getCodigoMCI() {
        return codigoMCI;
    }

    public void setCodigoMCI(long codigoMCI) {
        this.codigoMCI = codigoMCI;
    }

    public long getNumeroConvenio() {
        return numeroConvenio;
    }

    public void setNumeroConvenio(long numeroConvenio) {
        this.numeroConvenio = numeroConvenio;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
